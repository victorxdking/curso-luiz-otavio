// Escreva uma função que receba 2 números e retorne o maior deles
// 1º-------------------------------------------------------------------------------
function max(x, y) {
    if (x > y) {
        return x;
    } else {
        return y;
    }
}
console.log(max(10, 2));
// -------------------------------------------------------------------------------


// 2º-------------------------------------------------------------------------------
